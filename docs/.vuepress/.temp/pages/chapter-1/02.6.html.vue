<template><div><h1 id="_2-6-安装目录清单" tabindex="-1"><a class="header-anchor" href="#_2-6-安装目录清单"><span>2.6 安装目录清单</span></a></h1>
<p>你的 Go 安装目录 (<code v-pre>$GOROOT</code>) 的文件夹结构应该如下所示：</p>
<p>README.md, AUTHORS, CONTRIBUTORS, LICENSE</p>
<ul>
<li><code v-pre>/bin</code>：包含可执行文件，如：编译器，Go 工具</li>
<li><code v-pre>/doc</code>：包含文档模版</li>
<li><code v-pre>/lib</code>：包含示例程序，代码工具，本地文档等</li>
<li><code v-pre>/misc</code>：包含与支持 Go 编辑器有关的配置文件以及 cgo 的示例</li>
<li><code v-pre>/os_arch</code>：包含标准库的包的对象文件 (<code v-pre>.a</code>)</li>
<li><code v-pre>/src</code>：包含源代码构建脚本和标准库的包的完整源代码（Go 是一门开源语言）</li>
<li><code v-pre>/src/cmd</code>：包含 Go 和 C 的编译器和命令行脚本</li>
</ul>
